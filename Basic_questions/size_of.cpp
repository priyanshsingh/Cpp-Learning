#include <iostream>
using namespace std;

int main()
{
    cout << "\n\nThe size of int is = " << sizeof(int) << " bytes. \n\n";
    cout << "The size of float is = " << sizeof(float) << " bytes. \n\n";
    cout << "The size of double is = " << sizeof(double) << " bytes. \n\n";
    cout << "The size of char is = " << sizeof(char) << " bytes. \n\n";
    cout << "The size of wchar_t is = " << sizeof(wchar_t) << " bytes. \n\n";
    cout << "The size of bool is = " << sizeof(bool) << " bytes.\n\n";

    return 0;
}