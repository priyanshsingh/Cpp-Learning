#include <stdio.h>

int main()
{
    char x;
    printf("Enter a char = ");
    scanf("%c", &x);

    switch (x)
    {
    case 'a':

    case 'o':

    case 'i':

    case 'e':

    case 'u':

    case 'A':

    case 'E':

    case 'I':

    case 'O':

    case 'U':
        printf("A vowel");
        break;
    default:
        printf("Consonant");
        break;
    }
}